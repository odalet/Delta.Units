namespace Delta.Units
{
    /// <summary>
    /// Provides a few special characters that are not easy to type on a keyboard
    /// </summary>
    public static class SpecialCharacters
    {
        /// <summary>The upper case greek letter theta.</summary>
        public static readonly string Theta = "Θ"; // upper case
        
        /// <summary>The lower case greek letter theta.</summary>
        public static readonly string theta = "θ"; // lower case
        
        /// <summary>The lower case greek letter mu.</summary>
        public static readonly string mu = "μ"; // lower case

        /// <summary>The symbol for square: a '2' exponent.</summary>
        public static readonly string square = "²";
        
        /// <summary>The symbol for cubic: a '3' exponent.</summary>
        public static readonly string cubic = "³";

        /// <summary>The symbol for degree.</summary>
        public static readonly string degree = "°";

        /// <summary>The symbol for percent.</summary>
        public static readonly string percent = "%";
        
        /// <summary>The symbol for permille.</summary>
        public static readonly string permille = "‰";
    }
}
